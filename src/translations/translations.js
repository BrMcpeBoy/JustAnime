/**
 * Translate show type (TV, Movie, OVA, etc.)
 */
export const translateShowType = (showType, language) => {
  if (!showType) return '';
  const type = showType.toLowerCase();
  const key = translations[language]?.[type] || translations['en'][type];
  return key || showType;
};

/**
 * Translate and convert episode info (e.g., "EP 24" -> "бҹўбҹӨ")
 * Just convert numbers to Khmer, no "бһ—бһ¶бһӮ" prefix
 */
export const translateEpisodeInfo = (episodeInfo, language) => {
  if (!episodeInfo) return '';
  
  // Ensure episodeInfo is a string
  const episodeStr = String(episodeInfo);
  
  // For English, return as is
  if (language === 'en') return episodeStr;
  
  // Extract number from strings like "EP 1", "EP 24", "1", "24"
  const epMatch = episodeStr.match(/EP\s*(\d+)/i) || episodeStr.match(/(\d+)/);
  
  if (epMatch && epMatch[1]) {
    const epNumber = epMatch[1];
    return formatNumber(epNumber, language);
  }
  
  return episodeStr;
};

/**
 * Convert duration with Khmer numbers (e.g., "24min" -> "бҹўбҹӨбһ“бһ¶бһ‘бһё")
 */
export const translateDuration = (duration, language) => {
  if (!duration) return '';
  
  // For English, return as is
  if (language === 'en') return duration;
  
  const durationStr = String(duration);
  
  // Match patterns like "24min", "24m", "1hr 30min", "120m"
  // Replace "min" or "m" with Khmer "бһ“бһ¶бһ‘бһё"
  let result = durationStr.replace(/(\d+)\s*(min|m)/gi, (match, num) => {
    return `${formatNumber(num, language)}бһ“бһ¶бһ‘бһё`;
  });
  
  // Replace "hr" or "h" with Khmer if present
  result = result.replace(/(\d+)\s*(hr|h)/gi, (match, num) => {
    return `${formatNumber(num, language)}бһҳбҹүбҹ„бһ„`;
  });
  
  // If no unit found, just convert numbers
  if (result === durationStr) {
    result = durationStr.replace(/(\d+)/g, (match) => formatNumber(match, language));
  }
  
  return result;
};

/**
 * Translate quality (HD -> бһ…бҹ’бһ”бһ¶бһҹбҹӢ, FHD -> бһ…бҹ’бһ”бһ¶бһҹбҹӢбһҒбҹ’бһ–бһҹбҹӢ)
 */
export const translateQuality = (quality, language) => {
  if (!quality) return '';
  
  // For English, return as is
  if (language === 'en') return quality;
  
  const qualityStr = String(quality).toLowerCase();
  
  // Check for exact matches first
  if (qualityStr === 'hd') return getTranslation(language, 'hd');
  if (qualityStr === 'fhd') return getTranslation(language, 'fhd');
  
  // For patterns like "1080p", "4K", just convert numbers
  return String(quality).replace(/(\d+)/g, (match) => formatNumber(match, language));
};

